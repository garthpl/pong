public class ball {

    // instance variables
    private double rx, ry;        // position
    private double vx, vy;        // velocity
    private final double radius;  // radius

    // constructor
    public ball() {
        rx = 0.0;
        ry = 0.0;
        vx =  0.0;
        vy =  0.0;
        radius = 1.0;
    }
}